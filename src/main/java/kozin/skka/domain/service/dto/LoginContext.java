package kozin.skka.domain.service.dto;

import lombok.Data;
import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

import java.util.Collection;
import java.util.List;
@Data
public class LoginContext implements UserDetails {
    private UserForm userForm;
    private List<GrantedAuthority> authorities;

    public LoginContext(UserForm userForm, List<GrantedAuthority> authorities) {
        this.userForm = userForm;
        this.authorities = authorities;
    }

    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        return authorities;
    }

    @Override
    public String getPassword() {
        return userForm.getPassword();
    }

    @Override
    public String getUsername() {
        return userForm.getLoginId();
    }

    @Override
    public boolean isAccountNonExpired() {
        return true;
    }

    @Override
    public boolean isAccountNonLocked() {
        return true;
    }

    @Override
    public boolean isCredentialsNonExpired() {
        return true;
    }

    @Override
    public boolean isEnabled() {
        return true;
    }
}
